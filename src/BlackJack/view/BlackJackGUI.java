    public void getWinners(List<Card> player1, List<Card> player2, List<Card> player3, List<Card> crupier, List<Card> deck){
        List<String> result = new ArrayList<>();
        int player1result = Card.checkCards(player1);
        int player2result = Card.checkCards(player2);
        int player3result = Card.checkCards(player3);
        int crupierresult = Card.checkCards(crupier);
        
        if ((player1result > crupierresult && player1result <= 21) || (crupierresult > 21 && player1result <= 21)){
            result.add("Player1");
        }
        
        if ((player2result > crupierresult && player2result <= 21) || (crupierresult > 21 && player2result <= 21)){
            result.add("Player2");
        }
        
        if ((player3result > crupierresult && player3result <= 21) || (crupierresult > 21 && player3result <= 21)){
            result.add("Player3");
        }
        
        this.player1Text.setText("Player1: " + player1 + " " + player1result);
        this.player2Text.setText("Player2: " + player2 + " " + player2result);
        this.player3Text.setText("Player3: " + player3 + " " + player3result);
        this.crupierText.setText("Crupier: " + crupier + " " + crupierresult);
        this.deckText.setText("Deck: " + deck);
        
        this.winnersText.setText("" + result);
        
    }
    
    

